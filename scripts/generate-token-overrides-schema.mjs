/**
 * generate-token-overrides-schema.mjs  (Dynamic UI 2.6)
 *
 * Generates registry/schema/token-overrides.v1.json — the hand-authored JSON
 * Schema (draft 2020-12) describing the SHAPE of registry/token-overrides.json.
 * Referenced by the `$schema` field of that artifact.
 *
 * Versioning: a rebrand changes VALUES, not shape ⇒ does NOT bump this schema.
 * Bump to token-overrides.v{N}.json only when the document SHAPE changes.
 * Additive fields do not break consumers.
 *
 * Usage: node scripts/generate-token-overrides-schema.mjs
 */

import { writeFileSync, existsSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUTPUT_DIR = resolve(ROOT, 'registry');
const SCHEMA_DIR = resolve(OUTPUT_DIR, 'schema');
const SCHEMA_PATH = resolve(SCHEMA_DIR, 'token-overrides.v1.json');

const CDN_BASE = (process.env.CDN_BASE_URL ?? 'https://cdn.dynamicframework.dev/assets').replace(/\/$/, '');

const behaviorClass = { type: 'string', enum: ['override-base', 'override-leaf', 'follows', 'not-overridable'] };
const layer = { type: 'string', enum: ['palette', 'role', 'semantic', 'component'] };

// A single classified token node (base / step / spacing / radius / etc.).
const tokenNode = {
  type: 'object',
  required: ['cssVar', 'value', 'class'],
  properties: {
    cssVar: { type: 'string', pattern: '^--bs-' },
    value: { type: ['string', 'null'] },
    class: behaviorClass,
    layer,
    aliasOf: { type: 'string' },
    hasResponsiveRedef: { type: 'boolean' },
  },
  additionalProperties: false,
};

const rampNode = {
  type: 'object',
  required: ['kind', 'layer', 'base', 'steps', 'minimalRebrandSet', 'semanticFollowers'],
  properties: {
    kind: { type: 'string', enum: ['role', 'family', 'neutral'] },
    layer,
    hue: { type: 'string' },
    base: tokenNode,
    steps: {
      type: 'object',
      patternProperties: { '^[0-9]+$': tokenNode },
      additionalProperties: false,
    },
    minimalRebrandSet: { type: 'array', items: { type: 'string', pattern: '^--bs-' } },
    semanticFollowers: { type: 'array', items: { type: 'string', pattern: '^--bs-' } },
    rampMethod: { type: 'string' },
    rampWeights: { type: 'array', items: { type: 'number' } },
    method: { type: 'string' },
    regenerable: { type: 'boolean' },
  },
  additionalProperties: false,
};

const slotVar = {
  type: 'object',
  required: ['cssVar', 'referencedBy', 'suspected'],
  properties: {
    cssVar: { type: 'string', pattern: '^--bs-' },
    referencedBy: { type: 'array', items: { type: 'string' } },
    suspected: { type: 'boolean' },
    resolvesToIfFilled: { type: 'string' },
    note: { type: 'string' },
  },
  additionalProperties: false,
};

const schema = {
  $schema: 'https://json-schema.org/draft/2020-12/schema',
  $id: `${CDN_BASE}/schema/token-overrides.v1.json`,
  title: 'Dynamic UI — token-overrides.json contract v1',
  description: 'Describes the structure of registry/token-overrides.json: the additive, AI-consumable CSS custom-property override surface generated by the Dynamic UI build pipeline. NOT DTCG.',
  type: 'object',
  required: [
    '$schema', 'dynamicUi', 'generatedFrom', 'generatedAt',
    'consumption', 'behaviorClasses', 'color', 'typography',
    'spacing', 'radius', 'shadow', 'slots', 'gaps', 'componentTokens',
  ],
  properties: {
    $schema: { type: 'string', format: 'uri' },
    dynamicUi: { type: 'string' },
    generatedFrom: { type: 'string' },
    generatedAt: { type: 'string', format: 'date-time' },
    consumption: { type: 'object' },
    behaviorClasses: {
      type: 'object',
      required: ['override-base', 'override-leaf', 'follows', 'not-overridable'],
      additionalProperties: { type: 'string' },
    },
    color: {
      type: 'object',
      required: ['ramps', 'hueGroups', 'rebrandScope'],
      properties: {
        ramps: {
          type: 'object',
          patternProperties: { '^[a-z]+$': rampNode },
          additionalProperties: false,
        },
        hueGroups: {
          type: 'object',
          additionalProperties: { type: 'array', items: { type: 'string' } },
        },
        rebrandScope: {
          type: 'object',
          required: ['default'],
          additionalProperties: { type: 'string' },
        },
      },
      additionalProperties: false,
    },
    typography: {
      type: 'object',
      required: ['weights', 'lineHeight', 'scale'],
      properties: {
        fontFamily: tokenNode,
        fontMonospace: tokenNode,
        weights: { type: 'object', additionalProperties: tokenNode },
        lineHeight: { type: 'object', additionalProperties: tokenNode },
        scale: {
          type: 'object',
          properties: { note: { type: 'string' } },
          patternProperties: { '^fs-': tokenNode },
          additionalProperties: false,
        },
      },
      additionalProperties: false,
    },
    spacing: {
      type: 'object',
      required: ['levels'],
      properties: { note: { type: 'string' }, levels: { type: 'object', patternProperties: { '^[0-9]+$': tokenNode }, additionalProperties: false } },
      additionalProperties: false,
    },
    radius: {
      type: 'object',
      required: ['variants'],
      properties: { note: { type: 'string' }, variants: { type: 'object', additionalProperties: tokenNode } },
      additionalProperties: false,
    },
    shadow: {
      type: 'object',
      required: ['coverage', 'variants'],
      properties: { coverage: { type: 'string' }, note: { type: 'string' }, variants: { type: 'object', additionalProperties: tokenNode } },
      additionalProperties: false,
    },
    slots: {
      type: 'object',
      required: ['vars', 'denylist'],
      properties: {
        note: { type: 'string' },
        vars: { type: 'array', items: slotVar },
        denylist: { type: 'array', items: { type: 'string' } },
        denylistNote: { type: 'string' },
      },
      additionalProperties: false,
    },
    gaps: { type: 'object' },
    componentTokens: {
      type: 'object',
      required: ['status'],
      properties: {
        status: { type: 'string' },
        deferredCount: { type: 'integer' },
        note: { type: 'string' },
      },
      additionalProperties: false,
    },
  },
  additionalProperties: false,
};

if (!existsSync(OUTPUT_DIR)) mkdirSync(OUTPUT_DIR, { recursive: true });
if (!existsSync(SCHEMA_DIR)) mkdirSync(SCHEMA_DIR, { recursive: true });
writeFileSync(SCHEMA_PATH, `${JSON.stringify(schema, null, 2)}\n`);
process.stdout.write('Generated registry/schema/token-overrides.v1.json\n');
